export const getServiceByName = (name: string) => {
  return serviceTypes.find(service => service.name === name);
};

export const calculatePrice = (serviceName: string, startDate: Date, endDate: Date): number => {
  const service = getServiceByName(serviceName);
  if (!service) return 0;
  
  const days = Math.ceil((endDate.getTime() - startDate.getTime()) / (1000 * 60 * 60 * 24));
  return service.basePrice * days;
}; 